function getPlanetName(planetNumber){
    if(planetNumber == '1'){
      return 'Mercury'
    }else if(planetNumber == '2'){
      return 'Venus'
    }else if(planetNumber == '3'){
      return 'Earth'
    }else if(planetNumber == '4'){
      return 'Mars'
    }else if(planetNumber == '5'){
      return 'Jupiter'
    }else if(planetNumber == '6'){
      return 'Saturn'
    }else if(planetNumber == '7'){
      return 'Uranus'
    }else if(planetNumber == '8'){
      return 'Neptune'
    }
     
   }